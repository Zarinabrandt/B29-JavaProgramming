package day14_For_Loop;

public class For_Loop_Intro {
    public static void main(String[] args) {

        System.out.println("Hello World");

        System.out.println("-----------------------------------");


        for (int i = 1;  i <= 10; i++){ // i: 1↻ 2↻ 3↻ 4↻ .... 10
            System.out.println("Hello World"); // 1, 2, 3, 4 ....
        }

    }
}
