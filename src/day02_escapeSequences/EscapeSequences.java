package day02_escapeSequences;

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello students\nToday we have Java class");

        System.out.println("-----------------------------------------");

        System.out.println("\tJava Programming Language");
        System.out.println("\t\tJava Developer Course Batch 1");

        System.out.println("------------------------------------------");

        System.out.println("\\"); //it will print one backslash

        System.out.println("-------------------------------------------");

        System.out.println("Me favorite TV show is \"The Walking dead\"");

    }

}
/*
1. \n: new line
2. \t: paragraph space
3. \\: back slash
4. \": double quote
 */