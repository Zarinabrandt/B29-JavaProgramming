package day03;

public class PrimitiveDataTypesIntro {

    public static void main(String[] args) {

        //DataType variableName = Data;

        byte a = 39; // byte only accepts integer values (-128 ~ 127). Can not take decimal numbers
        System.out.println(a);

        //price of the car is $17500

        short price = 17500;
        System.out.println(price);

        //salary is $95000

        int s = 95000; //preferred data type
        System.out.println(s);

        long n = 9876543219L;
        System.out.println(n);

        //gpa is 3.5
        double gpa1 = 3.5; // preferred

        float gpa2 = 3.5F;
        System.out.println(gpa1);
        System.out.println(gpa2);





    }

}
